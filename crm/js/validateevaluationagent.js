$('#clarity')
    .on('mouseenter', function(){
       // alert('on');
       $('#clarityshow').hide();
       $('#clarityhide').show();
    })
    .on('mouseleave', function(){
       $('#clarityshow').show();
       $('#clarityhide').hide();
    })

